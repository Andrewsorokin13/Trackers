import UIKit

// MARK: Extension UIFont
extension UIFont {
    
    static func sfProBold(size: CGFloat) -> UIFont? {
        UIFont(name: "SFPro-Bold", size: size)
    }
    
    static func sfProRegular(size: CGFloat) -> UIFont? {
        UIFont(name: "SFPro-Regular", size: size)
    }
    
    static func sfProMedium(size: CGFloat) -> UIFont? {
        UIFont(name: "SFPro-Medium", size: size)
    }
}

